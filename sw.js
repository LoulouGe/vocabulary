const CACHE_NAME = 'vocab-v1';
const FILES_TO_CACHE = [
  '/vocabulary/',
  '/vocabulary/index.html',
  '/vocabulary/script.js',
  '/vocabulary/style.css',
  '/vocabulary/themes-de.js',
  '/vocabulary/themes-es.js',
  '/vocabulary/themes-zh.js',
  '/vocabulary/manifest.json',
  '/vocabulary/assets/IMG_3722.png'
];

// Install event - cache files
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(FILES_TO_CACHE);
    }).catch((error) => {
      console.log('Cache installation error:', error);
    })
  );
  self.skipWaiting();
});

// Activate event - clean up old caches
self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
  self.clients.claim();
});

// Fetch event - serve from cache, fallback to network
self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request)
      .then((response) => {
        if (response) {
          return response;
        }
        return fetch(event.request).then((response) => {
          if (!response || response.status !== 200 || response.type !== 'basic') {
            return response;
          }
          const responseToCache = response.clone();
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(event.request, responseToCache);
          });
          return response;
        });
      })
      .catch(() => {
        // Return offline page or cached response
        return caches.match('/vocabulary/index.html');
      })
  );
});
